#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	char	*i;
	int		n;

	n = ft_strlen(str);
	i = NULL;
	while (n != 0 && str[n] != c)
		n--;
	if (str[n] == c)
		i = (char*)(str + n);
	return (i);
}
