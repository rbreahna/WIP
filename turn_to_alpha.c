void	turn_to_alpha(char *tab)
{
	int i;
	int n;
	int x;

	i = 0;
	n = 0;
	x = 0;
	while (tab[i])
	{
		if (tab[i] == '#')
		{
			tab[i] = 'A' + x;
			n++;
		}
		if (n == 4)
		{
			n = 0;
			x++;
		}
		i++;
	}
	return ;
}
