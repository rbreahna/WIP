#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t i;
	size_t j;

	i = ft_strlen(dest);
	j = ft_strlen(dest);
	while ((i < ft_strlen(dest) + ft_strlen(src)) && src[i - j])
	{
		dest[i] = src[i - j];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
